function getDailyIndex(total: number): number {
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  return seed % total
}

function getCategoryColor(category: string): string {
  const map: Record<string, string> = {
    散文: 'bg-emerald-500/20 text-emerald-300 border-emerald-500/30',
    小说: 'bg-purple-500/20 text-purple-300 border-purple-500/30',
    童话: 'bg-amber-500/20 text-amber-300 border-amber-500/30',
  }
  return map[category] || 'bg-blue-500/20 text-blue-300 border-blue-500/30'
}

